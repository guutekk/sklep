function show_cart(){
    document.getElementById("koszyk").style.display = "block";
    document.getElementById("info").style.display= "none";
    document.getElementById("anuluj").style.display = "block";
    document.getElementById("zobacz").style.display = "none";
}

function hide_cart(){
    document.getElementById("koszyk").style.display = "none";
    document.getElementById("info").style.display= "block";
    document.getElementById("anuluj").style.display = "none";
    document.getElementById("zobacz").style.display = "block";
}